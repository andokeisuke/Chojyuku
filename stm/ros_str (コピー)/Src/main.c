int pid1(double error){

	int out;


	P1 = Kp1 * error;
	I1 += Ki1 * error * dt;
	D1 = Kd1 * (error - pre_error1) / dt;

	out = P1 + I1 + D1;

	pre_error1 = error;

	if(out>MAX_PWM){

		out = MAX_PWM;

	}else if(out<-MAX_PWM){

		out = -MAX_PWM;

	}

	if(now_deg > MAX_DEG || now_deg < -MAX_DEG)out = 0;



	return out;
}

int pid2(double error){

	int out;


	P2 = Kp2 * error;
	I2 += Ki2 * error * dt * 10;
	D2 = Kd2 * (error - pre_error2) / dt;

	out = P2 + I2 + D2;

	pre_error2 = error;

	if(out>MAX_PWM){

			out = MAX_PWM;

		}else if(out<-MAX_PWM){

			out = -MAX_PWM;

		}



	return out;
}


